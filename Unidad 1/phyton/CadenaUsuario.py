# 1. Solicitar la cadena de texto al usuario
cadena_original = input("Introduce una cadena de texto: ")

# 2. Convertir la cadena a mayúsculas usando el método .upper()
cadena_mayusculas = cadena_original.upper()

# 3. Mostrar el resultado
print(f"La cadena en mayúsculas es: {cadena_mayusculas}")
