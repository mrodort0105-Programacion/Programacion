nombre_completo = ["Fran", "Gomez"]

# División: Nombre es el primer elemento (índice 0)
nombre = nombre_completo[0:1] 
# División: Apellido es desde el índice 1 hasta el final
apellido = nombre_completo[1:] 

print(f"Array Inicial: {nombre_completo}")
print(f"Array Nombre: {nombre}")
print(f"Array Apellido: {apellido}")