# 1. Solicitar y leer el primer número, convirtiéndolo a entero.
num1 = int(input("Introduce el primer número entero: "))

# 2. Solicitar y leer el segundo número, convirtiéndolo a entero.
num2 = int(input("Introduce el segundo número entero: "))

# 3. Realizar la suma y guardar el resultado.
resultado = num1 + num2

# 4. Mostrar el resultado por pantalla.
# Se usa una f-string (cadena formateada) para incluir las variables fácilmente.
print(f"La suma de {num1} y {num2} es: {resultado}")
